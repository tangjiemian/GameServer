package com.common.beans;

public class ENpc {

	private String id;
	
	private String name;
	
	private byte type;
	
	private String animat;
	
	private String script;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public byte getType() {
		return type;
	}

	public void setType(byte type) {
		this.type = type;
	}

	public String getAnimat() {
		return animat;
	}

	public void setAnimat(String animat) {
		this.animat = animat;
	}

	public String getScript() {
		return script;
	}

	public void setScript(String script) {
		this.script = script;
	}
	
	

	
}
